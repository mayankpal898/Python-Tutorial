# Built-in Data Types
# Variables can store data of different types, and different types can do different things.
# str
x = "Hello World"
print(x)
print(type(x))
# int
x = 20
print(x)
print(type(x))
# float
x = 20.7
print(x)
print(type(x))
# complex
x = 1-8j
print(x)
print(type(x))
# list
x = ["apple", "banana", "cherry"]
print(x)
print(type(x))
# tuple
x = ("apple", "banana", "cherry")
print(x)
print(type(x))
# range
x = range(6)
print(x)
print(type(x))
# dict
x = {"name" : "John", "age" : 36}
print(x)
print(type(x))
# set
x = {"apple", "banana", "cherry"}
print(x)
print(type(x))
# bool
x = True
print(x)
print(type(x))




# Casting
# x will be 1
x = int(1) 
# y will be 2
y = int(2.8) 
# z will be 3
z = int("3") 
print(x)
print(y)
print(z)
# x will be 1.0
x = float(1)     
# y will be 2.8
y = float(2.8)   
# z will be 3.0
z = float("3")   
# w will be 4.2
w = float("4.2") 
print(x)
print(y)
print(z)
print(w)
# x will be 's1'
x = str("s1") 
# y will be '2'
y = str(2)    
# z will be '3.0'
z = str(3.0)  
print(x)
print(y)
print(z)
